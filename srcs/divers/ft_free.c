#include "cub3d.h"

void	ft_free_tab(void **tab)
{
	int	a;

	a = 0;
	while (tab[a] != NULL)
	{
		free(tab[a]);
		tab[a] = 0;
		a++;
	}
	free(tab);
	tab = 0;
}
